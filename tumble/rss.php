				<title>Review: LED Lenser M1</title>
				<description><![CDATA[	<p><a href="http://www.trigami.com/?blog=http://disturbed-reality.de/tumble/">Trigami-Review</a></p>

	<p>Leider erst direkt nach den Sommerferien hat mich ein Paket mit einigen <a href="http://www.ledlenser.com/">LED-Taschenlampen</a>, erreicht. Schade, so konnte ich die guten Stücke nicht mehr wie ursprünglich geplant in Ägypten (Hitze) und Schottland (Wasser) auf Herz und Nieren testen. Ich hab mir daher vorgenommen, die Situationen hier ein wenig nachzuspielen und für euch zu dokumentieren.</p>

	<p>Ok, aber erstmal vorne anfangen. Geschickt bekam ich von der Firma <a href="http://www.ledlenser.com/">Zweibrüder Optoelectronics</a> (<a href="http://www.ledlenser.com/community">Community</a> bzw. Forum) ein Paket, in dem ich die Lampen <a href="http://www.ledlenser.com/m1">Led Lenser M1</a>, <a href="http://www.ledlenser.com/m5">Led Lenser M5</a> und <a href="http://www.ledlenser.com/k3">Led Lenser K3</a> fand.</p>

	<p><img src="http://disturbed-reality.de/tumble/images/trigami_led/1.jpg" alt="" width="500" height="276" /></p>

	<p>In dem Test hier will ich besonders auf die <a href="http://www.ledlenser.com/m1">M1</a> (<span class="caps">UVP</span> 69€) eingehen. Mit ihr  dabei waren eine Nylonhülle, ein kleiner Karabinerhaken und ein Haufen Papierkram.</p>

	<p><img src="http://disturbed-reality.de/tumble/images/trigami_led/2.jpg" alt="" width="500" height="184" /><br />
Die Lampe selber ist klein und leicht und macht einen äußerst robusten Eindruck. Das Gehäuse ist auf Metall und wirkt edler als meine abgenudelte Taschenlampe von dem anderen großen Hersteller :)<br />
Die M1 kann in mehreren verschieden Modi betrieben werden und liefert dabei eine unterschiedliche Helligkeit. Geregelt wird dies über einen einzigen Knopf (bei allen drei Lampen gleich):<br />
<img src="http://disturbed-reality.de/tumble/images/trigami_led/3.jpg" alt="" width="500" height="315" /><br />
Bei gedrücktem Knopf leuchtet die Lampe kurzzeitig mit 190 Lumen Lichtleistung. Subjektiv empfinde ich das als so hell, dass man selbst bei Tageslicht nicht hinainschauen sollten.<br />
Drückt man den Knopf durch und schaltet die Lampe so ein, liefert sie 155 Lumen.<br />
Nach einigen Minuten Betriebsdauer schaltet sich die Lampe in den Energiesparmodus und leuchtet mit 60 Lumen. Dieser Modus kann auch durch einmaliges Antippen und anschließendes Einschalten des Knopfes aktiviert werden.<br />
Eine letzte Betriebsart startet man durch zweimaliges Antippen und anschließendes Einschalten: der Stroboskop. Mir ist zwar bis heute nicht bewusst, warum jeder Hersteller meint so etwas einbauen zu müssen, aber egal. Ich nutze es nicht und mir ist auch niemand bekannt, der diese Funktionalität bei einer Lampe nutzen würde.</p>

	<p>Den Fokus der Lampe regelt man über einen stufenloses Schiebesysten und kann so folgende Lichtstrahlen erzeugen:<br />
<img src="http://disturbed-reality.de/tumble/images/trigami_led/6.jpg" alt="" width="500" height="334" /> <br />
<img src="http://disturbed-reality.de/tumble/images/trigami_led/7.jpg" alt="" width="500" height="334" /></p>

	<p>Ihren Strom bezieht die Lampe aus einer CR123A Lithium-Batterie. Ich bin noch nie zuvor auf diesem Batterietyp gestoßen und daher ein klein wenig skeptisch wie diese sich denn im Preis und die Verfügbarkeit schlägt. ￼<br />
<img src="http://disturbed-reality.de/tumble/images/trigami_led/5.jpg" alt="" width="500" height="312" /><br />
Amazon bietet diesen Baterrietyp für 7,90€ im Doppelpack an, bei Conrad liegen eine(!) Batterie zwischen 5 und 10€. Diesen Preis find ich dann doch ganz stolz und nicht unbedingt gerechtfertigt. <br />
Einen direkten Vergleich kann ich hier zu der ebenfalls gelieferten M5 Lampe ziehen. Diese leuchtet zwar schwächer (88 Lumen) aber dafür auch um zwei Stunden länger. Und sie wird mit handelsüblichen AA-Batterien betrieben, die deutlich günstiger und besser zu bekommen sind. <br />
￼<br />
<img src="http://disturbed-reality.de/tumble/images/trigami_led/4.jpg" alt="" width="500" height="203" /></p>

	<p>Praxiseinsatz:<br />
Wie ich bereits geschrieben habe, konnte ich meinen urspünglichen Plan, die Lampe im Outdoor-Einsatz zu testen nicht in die Tat umsetzen, da mich das Paket erst nach den Ferien erreichte.<br />
Ich habe mich daher dazu entschlossen, die Lampe in simulierten Testumgebungen ein wenig zu beackern.</p>

	<p>Test-Szenario 1: Ägypten<br />
Im Urlaub war ich in Luxor. Luftfeuchtigkeit war keine vorhanden und die Temperaturen betrugen etwa 45° Celsius. Um dieses Szenario nachzustellen musste die gute Lamper in meinen Backofen wandern und dort bei 50° Celsius eine Weile vor sich hin leuchten. <br />
<img src="http://disturbed-reality.de/tumble/images/trigami_led/8.jpg" alt="" width="500" height="515" /><br />
Fazit: Finger verbrannt, erinnert worden den Backofen wieder zu putzen &#8211; aber Kamera tut ihren Dienst anstandslos.</p>

	<p>Test-Szenario 2: Schottland<br />
Direkt nach Ägypten gings für mich nach Schottland, wo ich es ein klein wenig feucht von oben, unten &#8211; eigentlich überall &#8211; war. Um dieses Szenario nachzustellen flog die Lampe kurzerhand in eine Schüssel voll Wasser und blieb dort einige Minuten liegen</p>

	<p><img src="http://disturbed-reality.de/tumble/images/trigami_led/9.jpg" alt="" width="500" height="437" /></p>

	<p>Fazit &#8211; Kamera tut ihren Dienst anstandslos.</p>

	<p>Nachdem die M1 die beiden Tests anstandslos überlebte, bin ich doch positiv überrascht. Aus der oben genannten Skepsis gegenüber den Batterien werde ich die M5 aus dem selben Haus sicherlich in den nächsten Ferien mit in die große weite Welt nehmen. Beides sind tolle Lampen, die zwar einen stolzen Preis haben, aber auch entsprechendes leisten.</p>]]></description>
